''' 1 - Parenteses tem a mais alta precedência
    2 - Exponenciação tem a segunda precedência
    3 - e operadores iguais são da esquerda para direita '''

print(2 ** 3 ** 2)     # o ** mais a direira e executado primeiro!
print((2 ** 3) ** 2)   # use parenteses para forçar a ordem desejada!

print(16 - 2 * 5 // 3 + 1)
print(2 ** 2 ** 3 * 3)

