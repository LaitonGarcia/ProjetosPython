''' Atualizar uma variável adicionando-se 1 é
chamado de incremento (increment); subtrair 1 é
chamado decremento (decrement). Alguns programadores
também falam sobre bumping uma variável,
que também significa incrementá-la de 1. '''

x = 6        # crie a variável x
print(x)
x = x + 1    # atualize x
print(x)

